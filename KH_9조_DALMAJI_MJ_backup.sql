/*테이블 생성*/
--이미진
CREATE TABLE BORROW (
   BORROW_NO           NUMBER          PRIMARY KEY
    ,BOOK_NO           NUMBER          NOT NULL
    ,MEMBER_NO           NUMBER        NOT NULL
    ,BORROW_DATE       TIMESTAMP       DEFAULT SYSDATE
    ,DUE_DATE           TIMESTAMP      DEFAULT SYSDATE +20
   ,RENEW               TIMESTAMP      
);
COMMENT ON COLUMN "BORROW"."BORROW_NO" IS '대여번호';
COMMENT ON COLUMN "BORROW"."BOOK_NO" IS '도서번호';
COMMENT ON COLUMN "BORROW"."MEMBER_NO" IS '회원번호';
COMMENT ON COLUMN "BORROW"."BORROW_DATE" IS '대출일자';
COMMENT ON COLUMN "BORROW"."DUE_DATE" IS '반납일자';
COMMENT ON COLUMN "BORROW"."RENEW" IS '대출연장';

CREATE TABLE OVERDUE (
   OVERDUE_NO           NUMBER              PRIMARY KEY
   , ADMIN_NO           NUMBER              NOT NULL
   , MEMBER_NO           NUMBER              NOT NULL
   , BORROW_NO           NUMBER              NOT NULL
    , STATUS_NO           NUMBER              NOT NULL
);

COMMENT ON COLUMN "OVERDUE"."OVERDUE_NO" IS '도서연체번호';
COMMENT ON COLUMN "OVERDUE"."ADMIN_NO" IS '관리자번호';
COMMENT ON COLUMN "OVERDUE"."MEMBER_NO" IS '회원번호';
COMMENT ON COLUMN "OVERDUE"."BORROW_NO" IS '대여번호';
COMMENT ON COLUMN "OVERDUE"."STATUS_NO" IS '도서상태번호';


CREATE TABLE BOOK_STATUS (
   STATUS_NO               NUMBER              PRIMARY KEY
   ,BOOK_STATE               VARCHAR2(1000)      NOT NULL
);
COMMENT ON COLUMN "BOOK_STATUS"."STATUS_NO" IS '도서상태번호';
COMMENT ON COLUMN "BOOK_STATUS"."BOOK_STATE" IS '도서상태';

--이제경
CREATE TABLE BOOK_LIST(
    BOOK_NO                  NUMBER          PRIMARY KEY
    , BOOK_CATE_NO           NUMBER          NOT NULL
    , STATUS_NO              NUMBER          DEFAULT 1
    , BOOK_ROOM_NO              NUMBER             
    , TITLE                  VARCHAR2(1000)     NOT NULL
    , AUTHOR                 VARCHAR2(1000)     NOT NULL
    , COMPANY                VARCHAR2(1000)     NOT NULL
    , CONT                   INT             NOT NULL
    , PUBLISHER_YEAR         TIMESTAMP       NOT NULL
    , BOOK_IMG               VARCHAR2(4000)  NOT NULL
);
COMMENT ON COLUMN "BOOK_LIST"."BOOK_NO" IS '도서번호';
COMMENT ON COLUMN "BOOK_LIST"."BOOK_CATE_NO" IS '책 카테고리 넘버';
COMMENT ON COLUMN "BOOK_LIST"."STATUS_NO" IS '도서상태';
COMMENT ON COLUMN "BOOK_LIST"."BOOK_ROOM_NO" IS '자료실 넘버';
COMMENT ON COLUMN "BOOK_LIST"."TITLE" IS '도서명';
COMMENT ON COLUMN "BOOK_LIST"."AUTHOR" IS '저자';
COMMENT ON COLUMN "BOOK_LIST"."COMPANY" IS '출판사';
COMMENT ON COLUMN "BOOK_LIST"."CONT" IS '대출횟수';
COMMENT ON COLUMN "BOOK_LIST"."PUBLISHER_YEAR" IS '발행년도';
COMMENT ON COLUMN "BOOK_LIST"."BOOK_IMG" IS '책 이미지';

CREATE TABLE BOOK_CATE(
    BOOK_CATE_NO                  NUMBER          PRIMARY KEY
    , CATE_NAME                  VARCHAR2(1000)   NOT NULL 
);
COMMENT ON COLUMN "BOOK_CATE"."BOOK_CATE_NO" IS '책 카테고리 넘버';
COMMENT ON COLUMN "BOOK_CATE"."CATE_NAME" IS '책 카테고리 이름';

--reference ROOM(자료실)
CREATE TABLE BOOK_ROOM (
   BOOK_ROOM_NO       NUMBER   PRIMARY KEY   
   ,ROOM_NAME   VARCHAR2(1000)   NOT NULL   
);
COMMENT ON COLUMN "BOOK_ROOM"."BOOK_ROOM_NO" IS '자료실 넘버';
COMMENT ON COLUMN "BOOK_ROOM"."ROOM_NAME" IS '자료실 이름';

--오의정
CREATE TABLE NOTICE (
    NO              NUMBER          PRIMARY KEY
    , ADMIN_NO      NUMBER          
    , TITLE         VARCHAR2(100)   NOT NULL
    , CONTENT       VARCHAR2(1000)  NOT NULL
    , ENROLL_DATE   TIMESTAMP       DEFAULT SYSDATE NOT NULL
    , MODIFY_DATE   TIMESTAMP       NULL
    , DEL_YN        CHAR(1)         DEFAULT 'N'     NOT NULL
    , HIT           NUMBER          DEFAULT 0       NOT NULL
--    , NOTICE_IMG    VARCHAR2(4000)  
);
COMMENT ON COLUMN "NOTICE"."NO" IS '번호';
COMMENT ON COLUMN "NOTICE"."ADMIN_NO" IS '관리자번호';
COMMENT ON COLUMN "NOTICE"."TITLE" IS '제목';
COMMENT ON COLUMN "NOTICE"."CONTENT" IS '내용';
COMMENT ON COLUMN "NOTICE"."ENROLL_DATE" IS '작성일자';
COMMENT ON COLUMN "NOTICE"."MODIFY_DATE" IS '수정일자';
COMMENT ON COLUMN "NOTICE"."DEL_YN" IS '삭제여부';
COMMENT ON COLUMN "NOTICE"."HIT" IS '조회수';
--COMMENT ON COLUMN "NOTICE"."NOTICE_IMG" IS '공지사항이미지';

CREATE TABLE B_OPTION (
   O_NO       NUMBER              PRIMARY KEY
   , B_OPTION       VARCHAR2(500)      NOT NULL
);
COMMENT ON COLUMN "B_OPTION"."O_NO" IS '대출가능번호';
COMMENT ON COLUMN "B_OPTION"."B_OPTION" IS '대출가능여부';

CREATE TABLE MEMBER(
    MEMBER_NO       NUMBER          PRIMARY KEY
    , O_NO          NUMBER          
    , NAME          VARCHAR2(100)   NOT NULL
    , ID            VARCHAR2(100)   NOT NULL
    , PWD           VARCHAR2(100)   NOT NULL
    , PHONE         CHAR(13)        NOT NULL
    , BORROW_PWD    CHAR(4)         DEFAULT 0        NOT NULL
    , OVERDUE_COUNT NUMBER          DEFAULT 0        NOT NULL
    , JOIN_DATE     TIMESTAMP       DEFAULT SYSDATE  NOT NULL
    , DEL_YN        CHAR(1)         DEFAULT 'N'      NOT NULL
);
COMMENT ON COLUMN "MEMBER"."NAME" IS '회원이름';
COMMENT ON COLUMN "MEMBER"."O_NO" IS '대출가능번호';
COMMENT ON COLUMN "MEMBER"."ID" IS '아이디';
COMMENT ON COLUMN "MEMBER"."PWD" IS '비밀번호';
COMMENT ON COLUMN "MEMBER"."PHONE" IS '휴대폰번호';
COMMENT ON COLUMN "MEMBER"."BORROW_PWD" IS '대출비밀번호';
COMMENT ON COLUMN "MEMBER"."OVERDUE_COUNT" IS '연체횟수';
COMMENT ON COLUMN "MEMBER"."JOIN_DATE" IS '가입일';
COMMENT ON COLUMN "MEMBER"."DEL_YN" IS '탈퇴여부';

CREATE TABLE ADMIN(
    ADMIN_NO    NUMBER          PRIMARY KEY
    , NAME      VARCHAR2(100)   NOT NULL
    , ID        VARCHAR2(100)   NOT NULL
    , PWD       VARCHAR2(100)   NOT NULL
    , DEL_YN    CHAR(1)         DEFAULT 'N' NOT NULL
);
COMMENT ON COLUMN "ADMIN"."ADMIN_NO" IS '번호';
COMMENT ON COLUMN "ADMIN"."NAME" IS '관리자이름';
COMMENT ON COLUMN "ADMIN"."ID" IS '아이디';
COMMENT ON COLUMN "ADMIN"."PWD" IS '비밀번호';
COMMENT ON COLUMN "ADMIN"."DEL_YN" IS '삭제여부';

